package 개인과제구상;

public class 점수 {
private String 이름;
private int 점수;
public 점수() {}
public 점수(String 이름, int 점수) {
	super();
	this.이름 = 이름;
	this.점수 = 점수;
}
public String get이름() {
	return 이름;
}
public void set이름(String 이름) {
	this.이름 = 이름;
}
public int get점수() {
	return 점수;
}
public void set점수(int 점수) {
	this.점수 = 점수;
}


}
