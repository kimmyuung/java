package 백준코딩문제;

import java.util.Scanner;

public class 백준코딩_3_5 {
public static void main(String[] args) {
	Scanner sc = new Scanner(System.in);
	System.out.println("숫자를 입력하세요");
	int ch = sc.nextInt();
	for(int i = 1; i < ch+1 ; i ++) {
		System.out.println(i);
	}
	sc.close();
	
}
}
